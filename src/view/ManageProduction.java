    private void fillCombo(){
        pcombo.removeAllItems();
        pname.removeAllItems();
        
        ArrayList<String> allproducts = new Controller().getProducts();
        
        for(int i=0; i<allproducts.size();i++){
            pcombo.addItem(allproducts.get(i));
            pname.addItem(allproducts.get(i));
        }
    }
    
    private void fillCombo1(){
        mcombo.removeAllItems();
        
        ArrayList<String> allmachines = new Controller().getMachines();
        
        for(int i=0; i<allmachines.size();i++){
            mcombo.addItem(allmachines.get(i));
        }
    }

