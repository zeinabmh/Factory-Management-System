    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String pname = pcombo.getSelectedItem().toString();
       String mname = mcombo.getSelectedItem().toString();
       String date = yearcombo.getSelectedItem().toString() + "-" + monthcombo.getSelectedItem().toString() + "-" + daycombo.getSelectedItem().toString();
       int q = Integer.parseInt(quantity.getText());
       int c = Integer.parseInt(cost.getText());
       
       int pid = new Controller().getPId(pname);
       int mid = new Controller().getMId(mname);
       
       new Controller().NewProduction(pid , mid , q , c , date);
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

